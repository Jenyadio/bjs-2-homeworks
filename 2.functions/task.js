function getArrayParams(arr) {
  let min = arr[0];
  let max = min;
  let sum = 0;

  for (let i = 0; i < arr.length; i++) {
    if (arr[i] > max) {
      max = arr[i];
    } else if (arr[i] < min) {
      min = arr[i];
    }
    sum += arr[i]
  }
  return {max: max, min: min, avg: +(sum / arr.length).toFixed(2)};
}

